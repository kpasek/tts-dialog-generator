    def clean_line(self, text):
        text = super().clean_line(text)
        text = re.sub(r"\[.*?\]\]", "", text)
        return text.strip()

    def tts_line(self, text):
        text = super().tts_line(text)
        for pattern, replacement in self.patterns:
            text = re.sub(pattern, replacement, text)
        return text.strip()
